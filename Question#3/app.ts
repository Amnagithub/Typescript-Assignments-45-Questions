// Q3:Name Cases: Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase.
//store a person name in a variable:

let person_name = ("eric");

//print in Uppercase:

console.log("UperCase :",person_name.toUpperCase());

//print in lowerCase:

console.log("LowerCase :",person_name.toLocaleLowerCase());

//print in TitleCase:

console.log("TitleCase :",person_name.charAt(0).toUpperCase()+ person_name.slice(1).toLowerCase());




